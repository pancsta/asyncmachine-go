// ExportedFields returns a slice of exported field names for a given struct.
func ExportedFields(obj interface{}) []string {
	var fieldNames []string

	// 1. Get the type of the interface
	t := reflect.TypeOf(obj)

	// 2. If it's a pointer, dereference it to get the underlying type
	if t.Kind() == reflect.Ptr {
		t = t.Elem()
	}

	// 3. Make sure we are actually dealing with a struct
	if t.Kind() != reflect.Struct {
		return fieldNames // Return empty slice if not a struct
	}

	// 4. Iterate over all fields in the struct type
	for i := 0; i < t.NumField(); i++ {
		field := t.Field(i)

		// 5. Skip unexported fields (lowercase) because a generated
		// mapper usually can't (and shouldn't) access them.
		if !field.IsExported() {
			continue
		}

		fieldNames = append(fieldNames, field.Name)
	}

	return fieldNames
}

func ToSuffix(s string) string {
	var builder strings.Builder

	for _, r := range s {
		if unicode.IsUpper(r) {
			builder.WriteRune(r) // Keep it uppercase
		}
	}

	return builder.String()
}

var camelCaseRe = regexp.MustCompile(`([a-z0-9])([A-Z])`)

func spaceCase(s string) string {
	// Insert a space between lowercase/number and uppercase letters
	spaced := camelCaseRe.ReplaceAllString(s, "$1 $2")

	// Convert the entire result to lowercase
	return strings.ToLower(spaced)
}
