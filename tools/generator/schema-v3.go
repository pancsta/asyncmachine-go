func WriteHandlers(schema string) error {
	path := strings.Split(os.Getenv("GOPACKAGE"), "/")
	name := path[len(path)-1] + ".gen.go"
	return os.WriteFile(name, []byte(schema), 0644)
}

type Handlers struct {
	States   S
	Handlers any
}

type Args struct {
	Id     string
	IdRpc  string
	Val    any
	ValRpc any
}

func GenHandlers(pkg string, args *Args, handlers ...Handlers) string {
	// header

	if pkg == "" {
		pkg = os.Getenv("GOPACKAGE")
	}

	f := jen.NewFile(pkg)
	f.HeaderComment("Code generated by am-gen; DO NOT EDIT.")
	f.HeaderComment("//go:build tinygo")

	amPath := "github.com/pancsta/asyncmachine-go/pkg/machine"
	f.ImportAlias(amPath, "am")

	// handlers

	for _, h := range handlers {

		states := h.States

		v := reflect.ValueOf(h.Handlers)
		if v.Kind() != reflect.Ptr || v.Elem().Kind() != reflect.Struct {
			panic("GenHandlers expects a pointer to a struct")
		}

		name := reflect.TypeOf(h.Handlers).Elem().Name()
		negs := jen.Dict{}
		finals := jen.Dict{}
		methods, err := am.ListHandlers(h.Handlers, states)
		if err != nil {
			panic(err)
		}

		for _, name := range methods {
			isFinal := strings.HasSuffix(name, am.SuffixState) ||
				strings.HasSuffix(name, am.SuffixEnd)
			if isFinal {
				finals[jen.Lit(name)] = jen.Id("h").Dot(name)
			} else {
				negs[jen.Lit(name)] = jen.Id("h").Dot(name)
			}
		}

		// func MachineHandlersNegotiations(h *MachineHandlers) map[string]am.HandlerNegotiation { ... }
		f.Func().Id(name+"Negotiations").
			Params(jen.Id("h").Op("*").Id(name)).
			Map(jen.String()).Qual(amPath, "HandlerNegotiation").
			Block(
				jen.Return(
					jen.Map(jen.String()).Qual(amPath, "HandlerNegotiation").Values(negs),
				),
			)

		f.Line()

		// func MachineHandlersFinals(h *MachineHandlers) map[string]am.HandlerFinal { ... }
		f.Func().Id(name+"Finals").
			Params(jen.Id("h").Op("*").Id(name)).
			Map(jen.String()).Qual(amPath, "HandlerFinal").
			Block(
				jen.Return(
					jen.Map(jen.String()).Qual(amPath, "HandlerFinal").Values(finals),
				),
			)
	}

	if args != nil {
		GenArgsToArgs(f, args)
	}

	// Output the generated code
	return fmt.Sprintf("%#v", f)
}

func GenArgsToArgs(f *jen.File, args *Args) {
	// RPC to Args
	dict := jen.Dict{}
	for _, field := range ExportedFields(args.ValRpc) {
		dict[jen.Id(field)] = jen.Id("argsRpc").Dot(field)
	}

	f.Comment("ArgsRpcToArgs copies identical fields from [" +
		args.IdRpc + "] to [" + args.Id + "].")
	f.Func().Id("ArgsRpcToArgs").
		Params(jen.Id("argsRpc").Op("*").Id(args.IdRpc)).
		Id(args.Id).
		Block(
			jen.Return(
				jen.Id(args.Id).Values(dict),
			),
		)

	// Args to RPC
	dict = jen.Dict{}
	for _, field := range ExportedFields(args.Val) {
		dict[jen.Id(field)] = jen.Id("args").Dot(field)
	}

	f.Comment("ArgsToArgsRpc copies identical fields from [" +
		args.Id + "] to [" + args.IdRpc + "].")
	f.Func().Id("ArgsToArgsRpc").
		Params(jen.Id("args").Op("*").Id(args.Id)).
		Id(args.IdRpc).
		Block(
			jen.Return(
				jen.Id(args.IdRpc).Values(dict),
			),
		)
}

